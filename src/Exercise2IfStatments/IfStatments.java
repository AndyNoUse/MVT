package Exercise2IfStatments;

public class IfStatments {

    public static void main(String[] args) {
        int firstValue = 8;
        int secondValue = 5;

        if(firstValue > secondValue) {
            System.out.println("firstValue is bigger than secondValue");
        } else if(firstValue == secondValue){
            System.out.println("firstValue is the same as secondValue");

        } else {
            System.out.println("firstValue is smaller than secondValue");
        }

        System.out.println("end of text");
    }


}

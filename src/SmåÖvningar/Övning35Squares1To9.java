package SmåÖvningar;

public class Övning35Squares1To9 {
    public static void main(String[] args) {
        /*.Skapa ett program som beräknar och skriver ut kvadraterna för talen 1 till 9.

         */
        int number = 1;
        while (number <= 9) {
            System.out.print(number * number + " ");
        number++;
        }
    }
}

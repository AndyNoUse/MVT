package Excercise13Switch;

import java.util.Scanner;

public class LongWords {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        String letter = scan.nextLine();

        switch (letter){
            case "A":
                System.out.println();
                break;
            case "B":
                System.out.println();
                break;
            case "C":
                System.out.println();
                break;
            case "D":
                System.out.println();
                break;

        }
    }
}

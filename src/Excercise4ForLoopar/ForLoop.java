package Excercise4ForLoopar;

public class ForLoop {
    public static void main(String[] args) {

        for (int day = 1; day <= 31; day++) {
            System.out.println(day + " January");
        }
        for (int i = 0; i < 31; i++) {
            System.out.println((i + 1) + "January");
        }
    }
}
